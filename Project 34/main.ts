const favoritePizzas: string[] = ["pepperoni", "cheese", "bbq"];

// Loop to print pizza names and statements
for (const pizza of favoritePizzas) {
  console.log(`I like ${pizza} pizza.`);
}

// Statement expressing love for pizza (outside the loop)
console.log("I really love pizza! My favorite combinations are pepperoni, cheese, and bbq, but I'm always open to trying new flavors.");
