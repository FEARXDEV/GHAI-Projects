let alien_color = 'red';

if (alien_color === 'green') {
  console.log("You just earned 5 points for shooting a green alien!");
} else if (alien_color === 'yellow') {
  console.log("You just earned 10 points for shooting a yellow alien!");
} else if (alien_color === 'red') {
  console.log("You just earned 15 points for shooting a red alien!");
} else {
  console.log("The alien is not green, yellow, or red.");
}